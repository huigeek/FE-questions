// 密码强度
// 最少6位，包括至少1个大写字母，1个小写字母，1个数字，1个特殊字符
// 用到正向前瞻表达式 (?=exp), 匹配后面满足exp表达式的位置
// (?=.{6,}) 后面满足 任意字符，至少6位
// (?=.*\d) 后面满足 任意数量的字符+数字
// (?=.*[A-Z]) 后面满足 任意数量的字符+大写
// (?=.*[a-z]) 后面满足 任意数量的字符+小写
// (?=.*[!@#$%^&*?]) 后面满足 任意数量的字符+特殊字符

const pattern = /^.*(?=.{6,})(?=.*\d)(?=.*[A-Z])(?=.*[a-z])(?=.*[!@#$%^&*?]).*$/

console.log(pattern.test('123')) // false
console.log(pattern.test('abc')) // false
console.log(pattern.test('A3a!00')) // true


// 位运算符

// & 按位与操作符，对两个运算元的每一个二进制位进行逻辑与操作

// | 按位或操作符，对两个运算元的每一个二进制位进行逻辑或操作

// ^ 按位异或操作符。对两个运算元的每一个二进制位进行异或操作。异或操作的意思是两个运算元不同时为true，相同则为false

// ~ 按位取反操作符。一元操作符，它对操作元的每一个二进制位取反。

// << 左移位操作符。将第一个操作元的二进制形式向左移第二个操作元个比特位，右边用0填充。移一位相当于乘以2，移两位相当于乘以4，依次类推。

// >> 有符号右移位操作符。将第一个操作元的二进制形式向右移第二个操作元个比特位，左边用符号位填充。

// >>> 元符号右移操作符。与有符号右移位类似，除了左边一律使用0补位。
